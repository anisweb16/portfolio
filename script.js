// ============================================
// i18n ENGINE
// ============================================
let currentLang = 'es';
let typedInstance = null;

function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;
    currentLang = lang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) el.innerHTML = t[key];
    });

    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
    });

    document.documentElement.setAttribute('lang', lang);
    document.title = lang === 'en'
        ? 'Portfolio – ANIS BEN AYED | DAM Developer'
        : 'Portfolio – ANIS BEN AYED | Desarrollador DAM';

    if (typedInstance) typedInstance.destroy();
    typedInstance = new Typed('.typed-text', {
        strings: t["typed.strings"],
        typeSpeed: 60, backSpeed: 40, loop: true, showCursor: true, cursorChar: '|'
    });
}

// ============================================
// LANG SWITCHER
// ============================================
document.addEventListener('DOMContentLoaded', () => {
    const langBtns = document.querySelectorAll('.lang-btn');

    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            if (lang === currentLang) return;
            langBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            applyTranslations(lang);
        });
    });

    typedInstance = new Typed('.typed-text', {
        strings: translations.es["typed.strings"],
        typeSpeed: 60, backSpeed: 40, loop: true, showCursor: true, cursorChar: '|'
    });
});

// ============================================
// HEADER SHRINK ON SCROLL
// ============================================
const header = document.querySelector('.header');

window.addEventListener('scroll', () => {
    if (window.scrollY > 30) {
        header.style.padding = "10px 60px";
        header.style.background = "rgba(0,0,0,0.75)";
    } else {
        header.style.padding = "16px 60px";
        header.style.background = "rgba(0,0,0,0.45)";
    }
});


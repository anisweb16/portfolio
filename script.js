// VALIDACIÓN FORMULARIO
// ============================================
const form = document.getElementById("form");
const msgOk = document.getElementById("form-msg");
const errorNombre     = document.getElementById("error-nombre");
const errorEmail      = document.getElementById("error-email");
const errorTelefono   = document.getElementById("error-telefono");
const errorAsunto     = document.getElementById("error-asunto");
const errorMensaje    = document.getElementById("error-mensaje");
const errorPrivacidad = document.getElementById("error-privacidad");

form.addEventListener("submit", (e) => {
    e.preventDefault();
    const t = translations[currentLang];

    let nombre     = document.getElementById("nombre").value.trim();
    let email      = document.getElementById("email").value.trim();
    let telefono   = document.getElementById("telefono").value.trim();
    let asunto     = document.getElementById("asunto").value;
    let mensaje    = document.getElementById("mensaje").value.trim();
    let privacidad = document.getElementById("privacidad").checked;
    let hayError   = false;

    errorNombre.textContent = "";
    errorEmail.textContent = "";
    errorTelefono.textContent = "";
    errorAsunto.textContent = "";
    errorMensaje.textContent = "";
    errorPrivacidad.textContent = "";
    msgOk.classList.add("hidden");

    if (!nombre) { errorNombre.textContent = t["err.nombre"]; hayError = true; }
    if (!email)  { errorEmail.textContent  = t["err.email"];  hayError = true; }
    else if (!email.includes("@") || !email.includes(".")) { errorEmail.textContent = t["err.email_inv"]; hayError = true; }
    if (telefono && telefono.length < 9) { errorTelefono.textContent = t["err.telefono"]; hayError = true; }
    if (!asunto)  { errorAsunto.textContent  = t["err.asunto"];  hayError = true; }
    if (!mensaje) { errorMensaje.textContent = t["err.mensaje"]; hayError = true; }
    else if (mensaje.length < 10) { errorMensaje.textContent = t["err.msg_short"]; hayError = true; }
    if (!privacidad) { errorPrivacidad.textContent = t["err.privacidad"]; hayError = true; }

    if (hayError) return;
    msgOk.classList.remove("hidden");
    form.reset();
});

